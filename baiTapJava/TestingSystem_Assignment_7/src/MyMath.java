public  class MyMath {
    public static int max(int a, int b) {
        final int i = a > b ? a : b;
        return i;
    }
    public static int min(int c, int d) {
        final int i = c < d ? c : d;
        return i;
    }
    public static int sum(int a, int b) {
        return a + b;
    }

}
